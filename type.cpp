#include <iostream>

using namespace std;

int main() 
{
    cout << "int size : " << sizeof(int) <<endl;
    cout << "long size : " << sizeof(long) <<endl;
    cout << "long long size : " << sizeof(long long) <<endl;
    cout << "double size : " << sizeof(double) <<endl;
    cout << "float size : " << sizeof(float) <<endl;

    return 0;
}
